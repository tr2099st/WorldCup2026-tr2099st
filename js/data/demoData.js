const match = ({
  id,
  utcDate,
  group,
  homeName,
  homeTla,
  awayName,
  awayTla,
  homeScore,
  awayScore,
  status = "FINISHED",
  isToday = false,
}) => ({
  id,
  utcDate,
  status,
  stage: "GROUP_STAGE",
  group,
  isToday,
  homeTeam: team(homeName, homeTla),
  awayTeam: team(awayName, awayTla),
  score: { home: homeScore, away: awayScore },
});

